package com.napier.sem;

/**
 * Represents an city
 */
public class City
{
    /**
     * City ID
     */
    public int ID;

    /**
     * City's Name
     */
    public String Name;

    /**
     * Country's code
     */
    public String CountryCode;

    /**
     * District
     */
    public String District;

    /**
     * City's population
     */
    public int Population;
}